def seed():
    db = sessionlocal()
    added = skipped = 0
    for qd in ALL:
        if not db.query(GameQuestion).filter(
            GameQuestion.game_name==qd['game_name'],
            GameQuestion.question_text==qd['q'],
            GameQuestion.difficulty==qd['difficulty']).first():
            db.add(GameQuestion(
                game_name=qd['game_name'], question_text=qd['q'],
                options=qd['opts'], correct_answer=qd['correct'],
                difficulty=qd['difficulty'], section_number=qd['section_number'],
                formula=qd.get('formula',''), explanation=qd.get('explanation',''),
            )); added += 1
        else: skipped += 1
    db.commit(); db.close()
    print(f'✅ Added: {added}, Skipped: {skipped}')

if __name__ == '__main__':
    print(f'Seeding {len(ALL)} questions...')
    seed()
