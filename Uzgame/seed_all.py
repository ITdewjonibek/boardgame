def seed_all():
    db = sessionLocal()
    added = 0
    skipped = 0
    for q_data in ALL_QUESTIONS:
        exists = db.query(GameQuestion).filter(
            GameQuestion.game_name == q_data["game_name"],
            GameQuestion.question_text == q_data["q"],
            GameQuestion.difficulty == q_data["difficulty"],
        ).first()
        if not exists:
            obj = GameQuestion(
                game_name=q_data["game_name"],
                question_text=q_data["q"],
                options=q_data["opts"],
                correct_answer=q_data["correct"],
                difficulty=q_data["difficulty"],
                section_number=q_data["section_number"],
                formula=q_data.get("formula",""),
                explanation=q_data.get("explanation",""),
            )
            db.add(obj)
            added += 1
        else:
            skipped += 1
    db.commit()
    db.close()
    print(f"✅ Done! Added: {added}, Skipped (already exists): {skipped}")

if __name__ == "__main__":
    print(f"Seeding {len(ALL_QUESTIONS)} questions...")
    seed_all()
